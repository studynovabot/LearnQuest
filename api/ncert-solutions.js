// Simple NCERT Solutions API without complex dependencies
export default async function handler(req, res) {
  // Handle CORS
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, POST, PUT, DELETE, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization, X-User-Tier');

  if (req.method === 'OPTIONS') {
    return res.status(200).end();
  }

  try {
    const { board, class: className, subject, chapter, enhanced } = req.query;
    const userTier = req.headers['x-user-tier'] || 'free';

    // Check if enhanced features are requested
    if (enhanced === 'true' || req.url.includes('enhanced-ncert-solutions')) {
      // Block free tier users from enhanced features
      if (userTier === 'free') {
        return res.status(403).json({
          success: false,
          message: 'Enhanced NCERT Solutions require Pro or Goat tier subscription',
          error: 'TIER_RESTRICTION',
          requiredTier: 'pro'
        });
      }
    }

    // Mock NCERT solutions data for testing
    const mockSolutions = [
      {
        id: '1',
        board: board || 'cbse',
        class: className || '10',  
        subject: subject || 'science',
        chapter: chapter || 'light-reflection-refraction',
        question: 'Define the principal focus of a concave mirror.',
        answer: 'The principal focus of a concave mirror is the point on the principal axis where light rays parallel to the principal axis converge after reflection from the mirror.',
        type: 'theory',
        difficulty: 'medium',
        createdAt: new Date().toISOString()
      },
      {
        id: '2',
        board: board || 'cbse',
        class: className || '10',
        subject: subject || 'science', 
        chapter: chapter || 'light-reflection-refraction',
        question: 'What is the focal length of a plane mirror?',
        answer: 'The focal length of a plane mirror is infinity because parallel rays of light reflected from a plane mirror never converge or appear to converge.',
        type: 'theory',
        difficulty: 'easy',
        createdAt: new Date().toISOString()
      }
    ];

    // Enhanced features for pro/goat users
    if (enhanced === 'true' && (userTier === 'pro' || userTier === 'goat')) {
      // Add AI explanations for enhanced version
      mockSolutions.forEach(solution => {
        solution.aiExplanation = {
          stepByStep: 'Detailed step-by-step explanation would be generated by AI',
          keyConcepts: ['Reflection', 'Principal axis', 'Focal point'],
          tips: 'Remember that concave mirrors converge light rays',
          relatedTopics: ['Convex mirrors', 'Lens optics', 'Ray diagrams']
        };
      });
    }

    return res.status(200).json({
      success: true,
      message: enhanced === 'true' ? 'Enhanced NCERT solutions retrieved successfully' : 'NCERT solutions retrieved successfully',
      data: {
        solutions: mockSolutions,
        totalCount: mockSolutions.length,
        filters: {
          board: board || 'cbse',
          class: className || '10',
          subject: subject || 'science',
          chapter: chapter || 'all'
        },
        userTier,
        enhanced: enhanced === 'true'
      },
      timestamp: new Date().toISOString()
    });

  } catch (error) {
    console.error('NCERT Solutions API Error:', error);
    return res.status(500).json({
      success: false,
      message: 'Failed to retrieve NCERT solutions',
      error: error.message,
      timestamp: new Date().toISOString()
    });
  }
}